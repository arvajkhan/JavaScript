// ================= Search Student =================

function searchStudent() {

    const v = document
        .getElementById("search")
        .value
        .toLowerCase();

    const result = arr.filter((x) =>

        x.roll.includes(v) ||

        x.name.toLowerCase().includes(v) ||

        x.department.toLowerCase().includes(v) ||

        x.email.toLowerCase().includes(v) ||

        x.phone.includes(v)

    );

    render(result);
}

